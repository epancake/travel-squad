class InviteFriends extends React.Component {
  constructor(props){
    super(props)
    this.state = {
      isOpened: this.props.status,
    }
  }

  componentDidMount(){
  }

  randomEmoji = () => {
    return emojis[Math.floor(Math.random() * emojis.length)];
  };

  getUsers(){
    if (this.props.currentGroup) {
      return this.props.users.map(user => {
        if (user.group_id == window.location.href.slice(-9)) {
          return <li key={user.id} className="person">{this.randomEmoji() + user.fname + " " + user.lname}</li>
        }
      })
    }
  }

  render(){
    return (
      <div className="invite-div">
        <h2>Group:</h2>
        <div className="container">
        <div className="groupList">
          <p>Group Members:</p>
          <ul>{this.getUsers()}</ul>
        </div>
        <div>
          <section className="invite-friends" onClick={(e)=>{this.setState({ isOpened: !this.state.isOpened })}}>
          <p>Add Group Members</p>
          <h3 className={this.state.isOpened ? "hidden" : ""}>
            <i className="fas fa-plus-square"></i>
          </h3>
          <h3 className={!this.state.isOpened ? "hidden" : ""}>
            <i className="fas fa-minus-square"></i>
          </h3>
          </section>
          <Collapse className="invite-form" isOpened={this.state.isOpened}>
            <InviteForm key="1"  reFetchData={this.props.reFetchData} users={this.props.users}/>
          </Collapse>
        </div>

        </div>

      </div>
    )
  }
}

export default InviteFriends
